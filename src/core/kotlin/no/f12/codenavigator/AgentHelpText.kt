    }

    private fun generateInterpretation(tool: BuildTool): String = buildString {
        val t = { goal: String -> tool.taskName(goal) }
        val p = { name: String, value: String -> tool.param(name, value) }

        appendLine("=== code-navigator: Result Interpretation ===")
        appendLine()
        appendLine("Fan-in (${t("complexity")} / ${t("rank")}):")
        appendLine("- High fan-in on a concrete class (>20 callers) → possible god object or central")
        appendLine("  service. Investigate whether it has too many responsibilities.")
        appendLine("- High fan-in on an interface or abstract class → normal. Core domain abstractions")
        appendLine("  are meant to be widely depended on.")
        appendLine("- High fan-in on a repository → normal. Repositories are shared infrastructure.")
        appendLine()
        appendLine("Fan-out (${t("complexity")}):")
        appendLine("- High fan-out (>15 distinct dependencies) → class depends on too many things.")
        appendLine("  Candidate for decomposition — split into focused collaborators.")
        appendLine("- High fan-out in a formatter or controller → often acceptable. These are")
        appendLine("  integration points that coordinate many domain objects.")
        appendLine()
        appendLine("Dead code (${t("dead")}):")
        appendLine("- Each result includes a confidence level: HIGH, MEDIUM, or LOW.")
        appendLine("  HIGH = unreferenced in both production and test code.")
        appendLine("  MEDIUM = unreferenced in production but referenced in test code.")
        appendLine("  LOW = has framework annotations (likely invoked via reflection).")
        appendLine("- Each result includes a reason: NO_REFERENCES or TEST_ONLY.")
        appendLine("  NO_REFERENCES = no incoming references at all.")
        appendLine("  TEST_ONLY = referenced only in test code, not production.")
        appendLine("- Use ${p("prod-only", "true")} to hide TEST_ONLY items and focus on truly unreferenced code.")
        appendLine("- Use ${p("test-only", "true")} to show only TEST_ONLY items (code referenced exclusively from tests).")
        appendLine("- All framework presets are active by default — no configuration needed for Spring, Quarkus, or other frameworks.")
        appendLine("  Use ${p("treat-as-dead", "<preset>")} to treat a specific framework's annotated code as potentially dead.")
        appendLine("  Available presets: ${FrameworkPresets.availablePresets().sorted().joinToString(", ")}.")
        appendLine("  Use ${p("treat-as-dead", "ALL")} to remove all framework protections.")
        appendLine("  Composite presets: 'quarkus' includes jaxrs + cdi + microprofile + jpa + jakarta + validation + jackson.")
        appendLine("  Entry-point annotations (e.g. @GetMapping, @Scheduled) → excluded from results.")
        appendLine("  Modifier annotations (e.g. @Transactional, @Cacheable) → reported with LOW confidence.")
        appendLine("  Supertype entry points (e.g. JpaRepository, PanacheRepository) → excluded from results.")
        appendLine("  Receiver type entry points (e.g. Ktor Route/Application extension functions) → excluded from results.")
        appendLine("- Use -Pexclude-annotated to suppress additional entry points not covered by presets.")
        appendLine("- Use ${p("exclude", "<regex>")} to exclude entire packages (e.g. generated protobuf code).")
        appendLine("- Companion object members and data class copy()/componentN() are auto-filtered.")
        appendLine()
        appendLine("Change coupling (${t("coupling")}):")
        appendLine("- High coupling degree (>60%) with many shared revisions → strong implicit dependency.")
        appendLine("  Consider extracting shared logic or making the dependency explicit.")
        appendLine("- High coupling degree but few shared revisions (<5) → may be coincidence.")
        appendLine("  Wait for more data before acting.")
        appendLine("- Test file coupled with its production file → expected, not a problem.")
        appendLine()
        appendLine("Hotspots (${t("hotspots")}):")
        appendLine("- Files with high revision count AND structural problems (high fan-out, cycles)")
        appendLine("  are priority refactoring targets — they change often and are hard to change safely.")
        appendLine("- Files with high revision count but clean structure → active development, not a problem.")
        appendLine()
        appendLine("DSM — Dependency Structure Matrix (${t("dsm")}):")
        appendLine("- Read as: row depends on column. Cell value = number of dependency references.")
        appendLine("- Cells below the diagonal = forward dependencies (normal, expected).")
        appendLine("- Cells above the diagonal = backward dependencies (potential cycles — review these).")
        appendLine("- A clean DSM is lower-triangular: all non-zero cells sit below the diagonal.")
        appendLine("- Dense rows (many non-zero cells) → package depends on too many others. Candidate")
        appendLine("  for decomposition or layering review.")
        appendLine("- Dense columns → package is depended on by many others (high fan-in). If it is a")
        appendLine("  stable abstraction (interface, domain model) this is fine. If it is an implementation")
        appendLine("  detail, it signals hidden coupling.")
        appendLine("- Symmetric pairs (A→B and B→A both non-zero) → mutual dependency / cycle between")
        appendLine("  packages. The DSM highlights these with a WARNING block listing class-level edges.")
        appendLine("- Use ${p("dsm-depth", "2")} to collapse deep packages and see higher-level structure.")
        appendLine()
        appendLine("Cycles (${t("cycles")}):")
        appendLine("- Uses Tarjan's algorithm to find strongly connected components (SCCs) in the")
        appendLine("  package dependency graph. Each SCC is a group of packages that mutually depend")
        appendLine("  on each other, directly or transitively.")
        appendLine("- A 2-package cycle (A↔B) → often fixable by inverting one dependency or extracting")
        appendLine("  a shared interface.")
        appendLine("- A large cycle (3+ packages) → architectural tangle. Map the edges to find the")
        appendLine("  weakest link (fewest references) and break there first.")
        appendLine("- Cycles in leaf packages (utilities, helpers) are low risk. Cycles spanning")
        appendLine("  architectural layers (api↔persistence, domain↔infra) are high risk.")
        appendLine()
        appendLine("Distance (${t("distance")}):")
        appendLine("- Structural distance = tree hops between two coupled packages in the package hierarchy.")
        appendLine("  distance=1 means sibling or parent/child packages (close, expected).")
        appendLine("  distance=2-3 means packages in nearby but different subtrees (normal for cross-cutting")
        appendLine("  concerns like api→service or service→persistence).")
        appendLine("  distance>=4 means packages in very different parts of the tree (long-range coupling —")
        appendLine("  review whether an interface or shared module should mediate).")
        appendLine("- High distance + high dependency count → tightly coupled across a wide gap. Priority")
        appendLine("  candidate for introducing an abstraction boundary or co-locating the packages.")
        appendLine("- Low distance + low dependency count → normal, healthy locality.")
        appendLine("- Use ${p("top", "20")} to focus on the most distant couplings first.")
        appendLine()
        appendLine("Strength (${t("strength")}):")
        appendLine("- Integration strength classifies how tightly coupled two packages are based on the")
        appendLine("  type of knowledge shared (Balanced Coupling theory, Vlad Khononov).")
        appendLine("  CONTRACT = depends on interface or abstract class (loosest coupling, easiest to change).")
        appendLine("  MODEL = depends on data class or record (medium coupling, shared data structure).")
        appendLine("  FUNCTIONAL = depends on concrete class (tightest coupling, hardest to change).")
        appendLine("- When a package pair has edges at multiple levels, the strongest level wins.")
        appendLine("- FUNCTIONAL dependencies between distant packages → priority refactoring target.")
        appendLine("  Consider introducing an interface or shared data model.")
        appendLine("- CONTRACT dependencies → healthy. Packages couple through abstractions.")
        appendLine("- Combine with ${t("distance")}: high distance + FUNCTIONAL strength = worst case.")
        appendLine("  Low distance + CONTRACT strength = best case.")
        appendLine()
        appendLine("Balance (${t("balance")}):")
        appendLine("- Composite verdict combining strength × distance × volatility per package pair.")
        appendLine("  DANGER = tightly coupled (FUNCTIONAL) + high distance + high volatility. Highest risk.")
        appendLine("  OVER_ENGINEERED = loosely coupled nearby packages. Consider simplifying or merging.")
        appendLine("  TOLERABLE = poor modularity but low volatility. Monitor for changes.")
        appendLine("  BALANCED = good modularity (strength and distance are complementary).")
        appendLine("- DANGER pairs are priority refactoring targets: co-locate packages or introduce contracts.")
        appendLine("- OVER_ENGINEERED pairs may indicate unnecessary abstraction layers between close packages.")
        appendLine("- Use ${t("balance")} as a starting point, then drill into ${t("strength")}, ${t("distance")},")
        appendLine("  and ${t("volatility")} for individual dimensions.")
        appendLine()
        appendLine("Rank (${t("rank")}):")
        appendLine("- Uses PageRank on the call graph to find structurally important types.")
        appendLine("- High rank = many transitive dependents. A change here has a large blast radius.")
        appendLine("- Top-ranked types that are concrete classes → candidates for interface extraction")
        appendLine("  to reduce coupling.")
        appendLine("- Top-ranked types that are interfaces or abstract classes → healthy; they are")
        appendLine("  designed to be widely depended on.")
        appendLine("- Compare rank with fan-in (${t("complexity")}): rank captures transitive importance,")
        appendLine("  fan-in captures direct dependents. A class with moderate fan-in but high rank is")
        appendLine("  a hidden linchpin.")
        appendLine()
        appendLine("Code age (${t("code-age")}):")
        appendLine("- Shows how many months since each file was last changed.")
        appendLine("- Old + stable + clean structure → mature code, leave it alone.")
        appendLine("- Old + frequently appearing in hotspots → legacy burden. Prioritize refactoring.")
        appendLine("- Young code across many files → active development area, expect churn.")
        appendLine()
        appendLine("Churn (${t("churn")}):")
        appendLine("- Shows lines added, deleted, and net change per file over git history.")
        appendLine("- High churn (large added+deleted) with low net change → code is being rewritten")
        appendLine("  repeatedly. Investigate design instability or unclear requirements.")
        appendLine("- High churn concentrated in a few files → these are volatility hotspots. Cross-reference")
        appendLine("  with ${t("complexity")} to see if they also have structural problems.")
        appendLine("- High churn in test files → often normal; tests evolve with the code.")
        appendLine()
        appendLine("Author analysis (${t("authors")}):")
        appendLine("- Files touched by many distinct authors → higher coordination cost and defect risk.")
        appendLine("- A module with 1-2 authors → strong ownership, lower defect rate.")
        appendLine("- Use alongside ${t("hotspots")} to find files that are both heavily changed and")
        appendLine("  lack clear ownership.")
        appendLine()
        appendLine("Metrics snapshot (${t("metrics")}):")
        appendLine("- Quick project health snapshot: package count, class count, dependency counts,")
        appendLine("  cycle count, average fan-in/fan-out.")
        appendLine("- Use as a baseline before and after refactoring to measure structural improvement.")
        appendLine("- Rising cycle count or average fan-out over time → architecture is degrading.")
        appendLine("- Falling cycle count → layering improvements are taking effect.")
        appendLine()
        appendLine("Layer check (${t("layer-check")}):")
        appendLine("- Config file (.cnav-layers.json) defines layers top-to-bottom = outermost to innermost.")
        appendLine("- Each layer has name patterns (globs: *Controller, *Service, *). First match wins.")
        appendLine("- Kt and Test suffixes are stripped automatically before matching. Write *Route, not *RouteKt.")
        appendLine("  OrderRouteKt matches *Route. OrderServiceTest matches *Service.")
        appendLine("- OUTWARD violation: class depends on a class in a higher (outer) layer.")
        appendLine("  Fix by introducing an interface in the lower layer or moving the dependency.")
        appendLine("- PEER violation: class exceeds peerLimit for same-layer dependencies.")
        appendLine("  Default peerLimit=0 (no peer deps). Set peerLimit=-1 for unlimited.")
        appendLine("- Common pattern: test classes reaching wiring layer for DI setup (TestContext).")
        appendLine("  Add testInfrastructure: true to the wiring layer to exempt test classes from OUTWARD checks.")
        appendLine("  Production code depending on a testInfrastructure layer is still a violation.")
        appendLine("- If a class doesn't match any specific pattern, add it explicitly to the right layer.")
        appendLine("  Example: E2ETestBase doesn't end in Test — add \"E2ETestBase\" to a test/wiring layer.")
    }

