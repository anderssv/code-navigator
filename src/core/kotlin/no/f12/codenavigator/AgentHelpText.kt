        appendLine("13. STRENGTH: Integration strength between coupled packages")
        appendLine("   ${u("strength")}                     # classify all dependency edges")
        appendLine("   ${u("strength", p("top", "20"))}              # top 20 strongest couplings")
        appendLine("   ${u("strength", p("package-filter", "com.example"))} # scope to a package prefix")
