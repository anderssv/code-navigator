        appendLine("    Parameters:")
        appendLine("      -Ppattern=<regex>  (required) Regex to match against interface names")
        appendLine("    Usage: ./gradlew cnavInterfaces -Ppattern=Repository")
        appendLine("    Usage: ./gradlew cnavInterfaces -Ppattern=\".*Client\"")
        appendLine("    Usage: ./gradlew cnavInterfaces -Ppattern=\"services\\.interfaces\\..*\"")
        appendLine()
