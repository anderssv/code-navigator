        appendLine("  cnavHelp")
        appendLine("    Shows this help text.")
        appendLine("    Usage: ./gradlew cnavHelp")
        appendLine()
        appendLine("  cnavListClasses")
        appendLine("    Lists all classes in the project and their source files.")
        appendLine("    Scans compiled bytecode from the main source set.")
        appendLine("    Usage: ./gradlew cnavListClasses")
        appendLine()
        appendLine("  cnavFindClass")
        appendLine("    Searches for classes matching a regex pattern.")
        appendLine("    Matches against both class name and source file path (case-insensitive).")
        appendLine("    Parameters:")
        appendLine("      -Ppattern=<regex>  (required) Regex to match against class names and source paths")
        appendLine("    Usage: ./gradlew cnavFindClass -Ppattern=Service")
        appendLine("    Usage: ./gradlew cnavFindClass -Ppattern=\".*Reset.*\"")
        appendLine("    Usage: ./gradlew cnavFindClass -Ppattern=\"domain\\..*\"")
        appendLine()
