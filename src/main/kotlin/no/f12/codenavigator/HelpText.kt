        appendLine("    Without parameters, shows all packages. With -Ppackage, filters to matching packages.")
        appendLine("    Parameters:")
        appendLine("      -Ppackage=<regex>  (optional) Regex to match against package names")
        appendLine("    Usage: ./gradlew cnavDeps")
        appendLine("    Usage: ./gradlew cnavDeps -Ppackage=services")
        appendLine("    Usage: ./gradlew cnavDeps -Ppackage=\"ktor\\.routes\"")
