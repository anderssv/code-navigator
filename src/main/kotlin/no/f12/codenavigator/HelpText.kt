        appendLine("    Scans compiled bytecode to build a call graph, then walks callers transitively.")
        appendLine("    Parameters:")
        appendLine("      -Pmethod=<regex>  (required) Regex to match against Class.method names")
        appendLine("      -Pdepth=<N>       (optional, default 3) Maximum depth of caller tree")
        appendLine("    Usage: ./gradlew cnavCallers -Pmethod=resetPassword")
        appendLine("    Usage: ./gradlew cnavCallers -Pmethod=\".*Service\\.find.*\"")
        appendLine("    Usage: ./gradlew cnavCallers -Pmethod=\"Repo\\.save\" -Pdepth=5")
        appendLine()
        appendLine("  cnavCallees")
        appendLine("    Shows what a method calls, displayed as an indented tree.")
        appendLine("    Scans compiled bytecode to build a call graph, then walks callees transitively.")
        appendLine("    Parameters:")
        appendLine("      -Pmethod=<regex>  (required) Regex to match against Class.method names")
        appendLine("      -Pdepth=<N>       (optional, default 3) Maximum depth of callee tree")
        appendLine("    Usage: ./gradlew cnavCallees -Pmethod=resetPassword")
        appendLine("    Usage: ./gradlew cnavCallees -Pmethod=\".*Controller\\.handle.*\"")
        appendLine("    Usage: ./gradlew cnavCallees -Pmethod=\"Service\\.create\" -Pdepth=5")
        appendLine()
        appendLine("  cnavClass")
        appendLine("    Shows class signature: fields, methods, superclass, and implemented interfaces.")
        appendLine("    Filters out constructors, synthetic methods, property accessors, and data class boilerplate.")
